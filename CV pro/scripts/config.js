function getConfig() {
    var config = {
        
        // Set it as 'true' or 'false'
        useAutoMap : true,
        
        // If the value of userAutoMap is set to 'true', this address value will be used to generate a map by Google.
        // But if it is set to 'false', this address will not be used to automatically generate the map.
        address : '80B Le Duan - Da Nang City'
        
    };
    
    return config;
}
